        /// <summary>
        /// Valids the data.
        /// </summary>
        /// <returns><c>true</c> if XXXX, <c>false</c> otherwise.</returns>
        protected override bool ValidData()
        {
           
            var detailContent = ResourceHelper.GetResourceValueByName("ResDetailContent");
            if (string.IsNullOrEmpty(RefNo))
            {
                XtraMessageBox.Show(ResourceHelper.GetResourceValueByName("ResReceiptRefNo"), detailContent,
                    MessageBoxButtons.OK, MessageBoxIcon.Error);
                txtRefNo.Focus();
                return false;
            }
            var paymentVoucherDetails = CaPaymentDetails;
            if (paymentVoucherDetails.Count == 0)
            {
                XtraMessageBox.Show(ResourceHelper.GetResourceValueByName("ResVoucherDetail"), detailContent,
                    MessageBoxButtons.OK,
                    MessageBoxIcon.Error);
                return false;
            }
            foreach (var caPaymentDetail in CaPaymentDetails)
            {
                var debitAccount = _accountModels.FirstOrDefault(c => c.AccountNumber == caPaymentDetail.DebitAccount);
                var creditAccount = _accountModels.FirstOrDefault(c => c.AccountNumber == caPaymentDetail.CreditAccount);
               
                if (caPaymentDetail.AmountOC == 0)
                {
                    XtraMessageBox.Show(ResourceHelper.GetResourceValueByName("ResFArmortizationDetailAmountOC"),
                                       ResourceHelper.GetResourceValueByName("ResDetailContent"), MessageBoxButtons.OK,
                                       MessageBoxIcon.Error);
                    return false;
                }

               
            }

            return true;
        }

